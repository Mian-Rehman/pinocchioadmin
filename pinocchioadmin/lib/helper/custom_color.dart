import 'dart:ui';

class CustomColor{
  Color mbackgroundColor() {
    return Color(0XFFF4ECE5);
  }

  Color mMainColor() {
    return Color(0XFF0801E3);
  }

  Color mSecondColor() {
    return Color(0XFFF4ECE5);
  }

  Color mlightGray() {
    return Color(0XFFF6F6F6);
  }

  Color transparent() {
    return Color.fromARGB(0, 244, 236, 229);
  }

  Color white()
  {
    return Color.fromARGB(255, 255, 255, 255);
  }
}